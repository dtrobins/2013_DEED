package sluce2.market;

import sluce2.landscape.component.Parcel;

/**
 * A Bid consists of a Parcel and a currency amount.
 * 
 * @author Meghan Hutchins
 *
 */
public class Bid
{
    /** The target Parcel of the Bid */
    private Parcel targetParcel;
    
    /** The target Parcel of the Bid */
    private double bidPrice;
    
    
    public Bid()
    {
	this.targetParcel = null;
	this.bidPrice = 0d;
    }
    
    public Bid(Parcel targetParcel, double bidPrice)
    {
	this.targetParcel = targetParcel;
	this.bidPrice = bidPrice;
    }

//--------------------------
// Accessors  	      
    
    public Parcel getTargetParcel()
    {
        return targetParcel;
    }

    public void setTargetParcel(Parcel targetParcel)
    {
        this.targetParcel = targetParcel;
    }

    public double getBidPrice()
    {
        return bidPrice;
    }

    public void setBidPrice(double bidPrice)
    {
        this.bidPrice = bidPrice;
    }
 
}
